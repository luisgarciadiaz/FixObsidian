def find_subfolder(cat_name, subfolder_map=None):
    if not cat_name:
        return None
    if subfolder_map is None:
        subfolder_map = DEFAULT_SUBFOLDER_MAP
    cat_lower = cat_name.lower().strip()
    for folder, keywords in subfolder_map.items():
        for kw in keywords:
            if kw in cat_lower or cat_lower in kw:
                return folder
    return None

def make_obsidian_content(author, title, category, isbn, original_name, file_uri, version="v1.5.0", series="", volume="", chapter=""):
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    cat_slug = slugify_category(category)
    cat_tag = make_category_tag(category)
    isbn_display = ""
    if isbn:
        isbn_clean = re.sub(r'[^0-9Xx]', '', isbn)
        if isbn_clean:
            isbn_display = f"\n- **ISBN:** [{isbn_clean}](https://openlibrary.org/isbn/{isbn_clean})"
    file_link = ""
    if file_uri:
        safe_uri = "file:///" + urllib.parse.quote(str(file_uri).replace("\\", "/"))
        file_link = f"\n> [!abstract] File Link\n> [Open Local File]({safe_uri})"
    series_line = ""
    if series:
        vol = f" #{volume}" if volume else ""
        series_line = f"\n- **Series:** {series}{vol}"
    chapter_line = ""
    if chapter:
        chapter_line = f"\n- **Chapter:** {chapter}"

    fm_series = f'\nseries: "{series}"' if series else ""
    fm_volume = f'\nvolume: "{volume}"' if volume else ""
    fm_chapter = f'\nchapter: "{chapter}"' if chapter else ""

    content = f"""---
title: "{title}"
author: "[[{author}]]"
category: {category}
tags: [vortexy, {cat_slug}]
isbn: "{isbn or ''}"{fm_series}{fm_volume}{fm_chapter}
date_organized: {timestamp}
vortexy_version: {version}
---

# {title}

- **Author:** [[{author}]]
- **Category:** {cat_tag}{isbn_display}{series_line}{chapter_line}
- **Original Name:** `{original_name}`{file_link}

---
*Generated by Vortexy Graph Architect*
"""
    return content

def create_obsidian_note(config, meta, skip_if_exists=True, note_index=None, target_vault=None, silent=False):
    if not config.get("obsidian_enabled"):
        return {"status": "error", "error": "Obsidian integration disabled"}
    vault_path = target_vault or config.get("obsidian_vault")
    if not vault_path:
        return {"status": "error", "error": "Vault path not set"}
    filename = meta.get("filename", "")
    skip_exts = (".vtt", ".srt", ".djvu", ".mp3", ".mp4", ".jpg", ".png")
    if filename.lower().endswith(skip_exts):
        return {"status": "exists"}
    author_raw = meta.get("author") or "Unknown Auto"
    if author_raw in ("Unknown", "Unknown Auto"):
        return {"status": "exists"}
    if not os.path.exists(vault_path):
        try:
            os.makedirs(vault_path, exist_ok=True)
        except Exception as e:
            return {"status": "error", "error": f"Could not create vault path: {e}"}
    author = normalize_author_name(author_raw)
    category = meta.get("category") or "Uncategorized"
    cat_name = category
    cats = config.get("categories", {})
    if isinstance(cats, dict) and category in cats:
        cat_info = cats[category]
        if isinstance(cat_info, dict):
            cat_name = cat_info.get("name", category)
    title = meta.get("new_name") or meta.get("filename")
    isbn = meta.get("isbn") or ""
    final_path = meta.get("final_path")
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    if author != author_raw:
        old_note_title = sanitize_filename(f"{author_raw} - {title}")
        old_note_path = os.path.join(vault_path, f"{old_note_title}.md")
        if os.path.exists(old_note_path):
            try:
                os.remove(old_note_path)
            except Exception:
                pass
    if title.lower().startswith(author.lower()):
        note_title = sanitize_filename(title)
    else:
        note_title = sanitize_filename(f"{author} - {title}")
    note_path = os.path.join(vault_path, f"{note_title}.md")
    note_name_md = f"{note_title}.md"
    if note_index is not None:
        if note_name_md in note_index:
            return {"status": "exists"}
    elif os.path.exists(note_path):
        return {"status": "exists"}
    content = make_obsidian_content(
        author=author,
        title=title,
        category=cat_name,
        isbn=isbn,
        original_name=filename,
        file_uri=final_path,
        version=config.get('version', 'latest')
    )
    try:
        with open(note_path, "w", encoding="utf-8") as f:
            f.write(content)
        if not silent:
            print(f"Note created: {os.path.basename(note_path)}")
        return {"status": "created"}
    except Exception as e:
        return {"status": "error", "error": str(e)}
