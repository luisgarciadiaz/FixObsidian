import os
import re
import urllib.parse
from datetime import datetime

def sanitize_filename(name):
    illegal = r'[\\/:*?"<>|]'
    clean = re.sub(illegal, "-", name)
    clean = re.sub(r'-+', "-", clean)
    clean = re.sub(r'\s+', " ", clean)
    return clean.strip()

def normalize_author_name(name):
    if not name or name.lower() in ("unknown", "desconocido", "unknown auto"):
        return "Unknown Auto"
    delimiters = r" & | and |;"
    sub_authors = re.split(delimiters, name.strip())
    healed_parts = []
    for part in sub_authors:
        s = part.strip()
        if "," in s and s.count(",") == 1:
            parts = s.split(",")
            if len(parts) == 2:
                s = f"{parts[1].strip()} {parts[0].strip()}"
        s = s.title()
        healed_parts.append(s)
    return " & ".join(healed_parts)

def slugify_category(cat_name):
    if not cat_name:
        return "uncategorized"
    slug = cat_name.lower().strip()
    slug = re.sub(r'[^a-z0-9]+', '-', slug)
    slug = slug.strip('-')
    return slug or "uncategorized"

def make_category_tag(cat_name):
    if not cat_name:
        return "#Uncategorized"
    tag = cat_name.strip()
    tag = re.sub(r'[^a-zA-Z0-9\u00C0-\u024f]+', '_', tag)
    tag = tag.strip('_')
    return f"#{tag}" if tag else "#Uncategorized"

